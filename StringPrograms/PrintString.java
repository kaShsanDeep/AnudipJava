package StringPrograms;

public class PrintString {

	public static void main(String[] args) {
		String name = "Anudip";
		
		int len = name.length();
		
		for (int i=0;i<5;i++) {
			 
			 System.out.println(name);
			 for (int j=len;j<len;j--) {
				 
				 System.out.println(name.charAt(j));
				 
				 
			 }
			 			 
		 }
		
		
		
		
		}
	}


